#!/usr/bin/env node
// prep.mjs — assemble the figma_execute payload from the per-reel folders in the
// CURRENT PROJECT (cwd). Globs reels/<id>/data.json + transcript.txt → _payload.js.
//   Run from inside a project dir:  node ~/.claude/skills/reel-insights/scripts/prep.mjs [flags]
//   Flags: --move-videos  --origin-y N  --origin-x N  --row-gap N  --ids a,b  --avatar-hash H
import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const SKILL = dirname(fileURLToPath(import.meta.url));
const ROOT = process.env.REEL_PROJECT || process.cwd();
const REELS_DIR = join(ROOT, "reels");
if (!existsSync(REELS_DIR)) { console.error("No reels/ in", ROOT, "— run new-reel.sh first, or cd into your project dir."); process.exit(1); }

const args = process.argv.slice(2);
const flag = (k, d) => { const i = args.indexOf("--" + k); return i >= 0 ? (args[i + 1] && !args[i + 1].startsWith("--") ? args[i + 1] : true) : d; };
const opts = {
  originY: Number(flag("origin-y", 4200)), originX: Number(flag("origin-x", 0)), rowGap: Number(flag("row-gap", 160)),
  moveVideos: !!flag("move-videos", false),
  videos: !!flag("videos", false),
  videoPort: String(flag("video-port", "9232")),
  avatarHash: String(flag("avatar-hash", process.env.REEL_AVATAR_HASH || "")),
  ids: (() => { const v = flag("ids", null); return v && v !== true ? String(v).split(",").map((s) => s.trim()) : null; })(),
};

let reels = readdirSync(REELS_DIR, { withFileTypes: true })
  .filter((d) => d.isDirectory() && existsSync(join(REELS_DIR, d.name, "data.json")))
  .map((d) => JSON.parse(readFileSync(join(REELS_DIR, d.name, "data.json"), "utf8")));
if (opts.ids) reels = reels.filter((r) => opts.ids.includes(r.id));
reels.sort((a, b) => (a.rank || 99) - (b.rank || 99));
if (!reels.length) { console.error("No reels matched."); process.exit(1); }

const scripts = {};
for (const r of reels) {
  const tp = join(REELS_DIR, r.id, (r.files && r.files.transcript) || "transcript.txt");
  try { scripts[r.id] = readFileSync(tp, "utf8").trim(); } catch { scripts[r.id] = ""; }
}

// project config (handle/followers/avatar) from reels.config.json if present
let cfg = {};
try { cfg = JSON.parse(readFileSync(join(ROOT, "reels.config.json"), "utf8")); } catch {}
const CTX = {
  avatarHash: opts.avatarHash || cfg.avatarHash || "",
  handle: cfg.handle || "@zeeeljain", followers: cfg.followers || "126K",
  originX: opts.originX, originY: opts.originY, rowGap: opts.rowGap, moveVideos: opts.moveVideos,
  videoBaseUrl: opts.videos ? ("http://localhost:" + opts.videoPort) : "",
};

const builder = readFileSync(join(SKILL, "build-section.js"), "utf8");
const header = "// ===== AUTO-GENERATED by reel-insights prep.mjs — paste into figma_execute =====\n" +
  "const REELS = " + JSON.stringify(reels) + ";\nconst SCRIPTS = " + JSON.stringify(scripts) + ";\nconst CTX = " + JSON.stringify(CTX) + ";\n\n";
mkdirSync(join(ROOT, ".reel-build"), { recursive: true });
const out = join(ROOT, ".reel-build", "_payload.js");
writeFileSync(out, header + builder);

console.log("✓ wrote", out);
console.log("  reels      :", reels.map((r) => r.id).join(", "));
console.log("  moveVideos :", CTX.moveVideos, " originY:", CTX.originY, " avatar:", CTX.avatarHash ? CTX.avatarHash.slice(0, 8) + "…" : "(none — grey placeholder)");
console.log("\nNext: paste the contents of .reel-build/_payload.js into figma-console figma_execute.");
